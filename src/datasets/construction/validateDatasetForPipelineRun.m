function validateDatasetForPipelineRun(dataset)
    % VALIDATEDATASETFORPIPELINERUN Validates pipeline-ready dataset structure.
    %
    % INPUT
    %  dataset struct with fields
    %      .Xtrain (ntrain x d double)
    %      .Xtest  (ntest x d double)
    %      .ytrain (ntrain x 1 double)
    %      .ytest  (ntest x 1 double)
    %      .ntrain (positive integer)
    %      .ntest  (positive integer)
    %      .d      (positive integer)
    %
    % OUTPUT
    %  None. Throws an error if validation fails.

    % -- Top-level type check --
    assert(isstruct(dataset) && isscalar(dataset), ...
        'dataset must be a scalar struct.');

    % -- Required field check --
    requiredFields = { ...
        'Xtrain', ...
        'Xtest', ...
        'ytrain', ...
        'ytest', ...
        'ntrain', ...
        'ntest', ...
        'd' ...
    };

    missingFields = requiredFields(~isfield(dataset, requiredFields));
    assert(isempty(missingFields), ...
        'dataset is missing required field(s): %s.', ...
        strjoin(missingFields, ', '));

    actualFields = fieldnames(dataset);
    assert(numel(actualFields) == numel(requiredFields), ...
        'dataset contains unexpected fields.');

    % -- Validate scalar metadata --
    assert(isPositiveIntegerScalar(dataset.ntrain), ...
        'dataset.ntrain must be a positive integer scalar.');

    assert(isPositiveIntegerScalar(dataset.ntest), ...
        'dataset.ntest must be a positive integer scalar.');

    assert(isPositiveIntegerScalar(dataset.d), ...
        'dataset.d must be a positive integer scalar.');

    % -- Validate feature matrices --
    assert(isnumeric(dataset.Xtrain) && ismatrix(dataset.Xtrain), ...
        'dataset.Xtrain must be a numeric 2D matrix.');

    assert(isnumeric(dataset.Xtest) && ismatrix(dataset.Xtest), ...
        'dataset.Xtest must be a numeric 2D matrix.');

    assert(~isempty(dataset.Xtrain), ...
        'dataset.Xtrain must be nonempty.');

    assert(~isempty(dataset.Xtest), ...
        'dataset.Xtest must be nonempty.');

    assert(all(isfinite(dataset.Xtrain), 'all'), ...
        'dataset.Xtrain must contain only finite values.');

    assert(all(isfinite(dataset.Xtest), 'all'), ...
        'dataset.Xtest must contain only finite values.');

    [ntrainObserved, dTrainObserved] = size(dataset.Xtrain);
    [ntestObserved, dTestObserved] = size(dataset.Xtest);

    assert(ntrainObserved == dataset.ntrain, ...
        ['dataset.ntrain does not match size(dataset.Xtrain, 1). ' ...
         'Expected %d, got %d.'], ntrainObserved, dataset.ntrain);

    assert(ntestObserved == dataset.ntest, ...
        ['dataset.ntest does not match size(dataset.Xtest, 1). ' ...
         'Expected %d, got %d.'], ntestObserved, dataset.ntest);

    assert(dTrainObserved == dataset.d, ...
        ['dataset.d does not match size(dataset.Xtrain, 2). ' ...
         'Expected %d, got %d.'], dTrainObserved, dataset.d);

    assert(dTestObserved == dataset.d, ...
        ['dataset.d does not match size(dataset.Xtest, 2). ' ...
         'Expected %d, got %d.'], dTestObserved, dataset.d);

    % -- Validate label vectors --
    assert(isnumeric(dataset.ytrain) && iscolumn(dataset.ytrain), ...
        'dataset.ytrain must be a numeric column vector.');

    assert(isnumeric(dataset.ytest) && iscolumn(dataset.ytest), ...
        'dataset.ytest must be a numeric column vector.');

    assert(all(isfinite(dataset.ytrain)), ...
        'dataset.ytrain must contain only finite values.');

    assert(all(isfinite(dataset.ytest)), ...
        'dataset.ytest must contain only finite values.');

    assert(numel(dataset.ytrain) == dataset.ntrain, ...
        ['Length of dataset.ytrain must equal dataset.ntrain. ' ...
         'Expected %d, got %d.'], dataset.ntrain, numel(dataset.ytrain));

    assert(numel(dataset.ytest) == dataset.ntest, ...
        ['Length of dataset.ytest must equal dataset.ntest. ' ...
         'Expected %d, got %d.'], dataset.ntest, numel(dataset.ytest));

    % -- Basic sanity checks on labels --
    assert(~isempty(dataset.ytrain), ...
        'dataset.ytrain must be nonempty.');

    assert(~isempty(dataset.ytest), ...
        'dataset.ytest must be nonempty.');
end


function tf = isPositiveIntegerScalar(x)
    % ISPOSITIVEINTEGERSCALAR Returns true if x is a positive integer scalar.

    tf = isnumeric(x) ...
        && isscalar(x) ...
        && isfinite(x) ...
        && (x > 0) ...
        && (mod(x, 1) == 0);
end